public class Insomnio{
    
    public int calcularTiempo(){
        int horaQueDurmio=2;
        int minutoQueDurmio=45;
        int horaQueDesperto=11;
        int minutoQueDespeto=25;
        int horaQueDurmioEnMinutos=horaQueDurmio*60+minutoQueDurmio;
        int horaQueDesperoEnMinutos=horaQueDesperto*60+minutoQueDespeto;
        int resultado=horaQueDesperoEnMinutos-horaQueDurmioEnMinutos;
        return resultado;
     
        
        
        
    
    
    
    
    
    
      
    }
}
